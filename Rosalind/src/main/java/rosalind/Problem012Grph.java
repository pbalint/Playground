				+ "ATCTGCCTTCTTTTAAGCCTGGGCGTTAACGACTACTAGCCACCCATGCATATGTGAAAG\r\n"
				+ "CCCTAAAGATCAGGCTATCAGGCTAGTGCAGGACA\r\n"
				+ "";
		int overlapLength = 3;
		Map<String, DNASequence> sequences = FastaReaderHelper.readFastaDNASequence(new ByteArrayInputStream(input.getBytes()));
		Map<String, List<DNASequence>> prefixes = new HashMap<>();
		Map<String, List<DNASequence>> suffixes = new HashMap<>();
		StringBuilder prefixBuilder = new StringBuilder(overlapLength);
		StringBuilder suffixBuilder = new StringBuilder(overlapLength);
		for (Entry<String, DNASequence> sequenceEntry: sequences.entrySet()) {
			DNASequence sequence = sequenceEntry.getValue();
			for (int i = 1; i <= overlapLength; i++) {
				prefixBuilder.append(sequence.getCompoundAt(i));
				suffixBuilder.append(sequence.getCompoundAt(sequence.getLength() - overlapLength + i));
			}
			List<DNASequence> prefixList = prefixes.get(prefixBuilder.toString());
			if (prefixList == null) {
				prefixList = new ArrayList<>();
				prefixes.put(prefixBuilder.toString(), prefixList);
			}
			prefixList.add(sequence);
			prefixBuilder.setLength(0);
			
			List<DNASequence> suffixList = suffixes.get(suffixBuilder.toString());
			if (suffixList == null) {
				suffixList = new ArrayList<>();
				suffixes.put(suffixBuilder.toString(), suffixList);
			}
			suffixList.add(sequence);
			suffixBuilder.setLength(0);
		}
		
		for (Entry<String, List<DNASequence>> suffixSetEntry: suffixes.entrySet()) {
			for (DNASequence suffixSequence: suffixSetEntry.getValue()) {
				List<DNASequence> matchingPrefixes = prefixes.get(suffixSetEntry.getKey());
				if (matchingPrefixes == null) continue;
				
				for (DNASequence matchingPrefixSequence: matchingPrefixes) {
					if (suffixSequence != matchingPrefixSequence) {
						System.out.println(suffixSequence.getOriginalHeader() + " " + matchingPrefixSequence.getOriginalHeader());
						//System.out.println(suffixSequence + " " + matchingPrefixSequence);
					}
				}
			}
		}
